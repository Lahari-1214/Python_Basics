'''convert one datatype into another type on his 
own. This is called type conversion or coercion. '''
#  python program to convert into decimal number system 
n1 = 0O17 
n2 = 0B1110010 
n3 = 0X1c2 
n = int(n1) 
print('Octal 17 = ', n) 
n = int(n2) 
print('Binary 1110010 = ', n) 
n = int(n3) 
print('Hexadecimal 1c2 = ', n) 
