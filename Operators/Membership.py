"""
The membership operators are useful to test for membership in a sequence such as 
strings, lists, tuples or dictionaries
1) in
2) not in
"""
# Working of in operator
names = ["Rani", "Yamini", "Sushmita", "Veena"]
for name in names: 
    print (name) 

# Working of not in operator
postal = {'Delhi': 110001, 'Chennai': 600001, 'Kolkata': 700001, 
'Bangalore':560001} 
for city in postal: 
    print(city, postal[city]) 